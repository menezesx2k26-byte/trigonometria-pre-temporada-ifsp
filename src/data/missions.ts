import type {
  ChoiceQuestion,
  MatchQuestion,
  Mission,
  MultiQuestion,
  QuestionOption,
} from "./types";

const letters = "abcdefghijklmnopqrstuvwxyz";

function options(labels: string[]): QuestionOption[] {
  return labels.map((label, index) => ({ id: letters[index], label }));
}

function choice(
  id: string,
  prompt: string,
  labels: string[],
  correctIndex: number,
  explanation: string,
  trap: string,
): ChoiceQuestion {
  return {
    id,
    type: "choice",
    prompt,
    options: options(labels),
    correctId: letters[correctIndex],
    explanation,
    trap,
  };
}

function multi(
  id: string,
  prompt: string,
  labels: string[],
  correctIndexes: number[],
  explanation: string,
  trap: string,
): MultiQuestion {
  return {
    id,
    type: "multi",
    prompt,
    options: options(labels),
    correctIds: correctIndexes.map((index) => letters[index]),
    explanation,
    trap,
  };
}

function match(
  id: string,
  prompt: string,
  rows: Array<[string, string]>,
  answerLabels: string[],
  explanation: string,
  trap: string,
): MatchQuestion {
  const answerOptions = options(answerLabels);
  return {
    id,
    type: "match",
    prompt,
    rows: rows.map(([left, answer], index) => ({
      id: `r${index + 1}`,
      left,
      correctOptionId:
        answerOptions.find((option) => option.label === answer)?.id ?? "",
    })),
    options: answerOptions,
    explanation,
    trap,
  };
}

